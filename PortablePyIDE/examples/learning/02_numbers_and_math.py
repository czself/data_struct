# 第 2 课：数字和计算
# Python 可以帮你做加减乘除。

apples = 6
friends = 3

print("我有", apples, "个苹果。")
print("来了", friends, "个朋友。")
print("每个人可以分到", apples / friends, "个苹果。")

price = 5
count = 4
total = price * count

print("每本练习本", price, "元。")
print("买", count, "本，一共", total, "元。")


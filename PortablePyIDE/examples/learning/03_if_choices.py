# 第 3 课：如果……就……
# if 可以让程序根据情况做选择。

score = 86

print("这次得分：", score)

if score >= 90:
    print("太棒了，已经很熟练了。")
elif score >= 60:
    print("通过了，再练一练会更稳。")
else:
    print("没关系，找出不会的地方再试一次。")

weather = "下雨"

if weather == "下雨":
    print("今天带伞出门。")
else:
    print("今天可以轻松出门。")

